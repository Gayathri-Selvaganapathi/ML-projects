def function_one(number_one, number_two):
    """multiplys two inputs

    :param x: first number
    :type x: int
    :param y: second number
    :type y: int
    :return: result of mutiplication of inputs
    :rtype: int
    """
    return number_one * number_two


def function_two(number_one, number_two):
    """adds two inputs and returns the result

    :param x: first number
    :type x: int
    :param y: second number
    :type y: int
    :return: sum of the inputs
    :rtype: int
    """
    return number_one + number_two
